const LANGUAGE_NAMES = {
  en: "English",
  tl: "Filipino (Tagalog)",
  bcl_central: "Bikol Sentral",
  bcl_rinconada: "Rinconada Bikol",
  bcl_albay: "Bikol Albay",
  bcl_catanduanes_north: "Bikol Norte Katanduwanes",
  bisakol: "Bisakol",
};

function getLanguageLabel(code) {
  return LANGUAGE_NAMES[code] || code;
}

function updateTextNodes(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = t(key);
  });
}

function updatePlaceholders(root = document) {
  root.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    node.setAttribute("placeholder", t(key));
  });
}

function updateHtmlNodes(root = document) {
  root.querySelectorAll("[data-i18n-html]").forEach((node) => {
    const key = node.dataset.i18nHtml;
    node.innerHTML = t(key);
  });
}

function updateAriaLabels(root = document) {
  root.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    const key = node.dataset.i18nAriaLabel;
    node.setAttribute("aria-label", t(key));
  });
}

function updateTitles(root = document) {
  root.querySelectorAll("[data-i18n-title]").forEach((node) => {
    const key = node.dataset.i18nTitle;
    node.setAttribute("title", t(key));
  });
}

function syncSelectors() {
  document.querySelectorAll("[data-language-selector]").forEach((select) => {
    if (!(select instanceof HTMLSelectElement)) return;

    if (select.options.length !== Object.keys(translations).length) {
      select.innerHTML = "";
      Object.keys(translations).forEach((code) => {
        const option = document.createElement("option");
        option.value = code;
        option.textContent = getLanguageLabel(code);
        select.appendChild(option);
      });
    }

    select.value = currentLang;
  });
}

function bindSelectors() {
  document.querySelectorAll("[data-language-selector]").forEach((select) => {
    if (!(select instanceof HTMLSelectElement)) return;
    if (select.dataset.i18nBound === "true") return;

    select.addEventListener("change", () => {
      setLang(select.value);
    });

    select.dataset.i18nBound = "true";
  });
}

function applyTranslations() {
  document.documentElement.lang = currentLang.replace(/_/g, "-");
  document.title = t(document.documentElement.dataset.pageTitleKey || "page_title");
  updateTextNodes();
  updateHtmlNodes();
  updatePlaceholders();
  updateAriaLabels();
  updateTitles();
  syncSelectors();
}

